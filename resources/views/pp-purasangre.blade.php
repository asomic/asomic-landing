      <h1>Política de privacidad<br> Pura Sangre CrossFit app</h1>
      Asomic diseñó y desarrolló la aplicación Pura Sangre Crossit como una aplicación gratuita. Este servicio es proporcionado por asomic sin costo alguno y
      está diseñado para ser utilizado tal como está.

      Esta página se utiliza para informar a los visitantes sobre las políticas de recopilación, uso y divulgación de su información personal si alguien decide
      usar la aplicación.

      Si decide utilizar la aplicación, entonces acepta la recopilación y el uso de la información en relación con esta política. La información personal que se recoge
      se utiliza para proporcionar y mejorar el servicio. No utilizaremos ni compartiremos su información con nadie, excepto como se describe en esta Política de privacidad.

      Los términos utilizados en esta Política de privacidad tienen los mismos significados que en nuestros Términos y condiciones, a los que se puede acceder en Pura Sangre CrossFit,
      a menos que se defina lo contrario en esta Política de privacidad.

      <h3>Recopilación y uso de información</h3>
      Para una mejor experiencia, mientras utiliza nuestro Servicio, podemos solicitar que nos proporcione cierta información de identificación personal, que incluye, entre otros,
      el nombre, correo electrónico, número de teléfono y datos de rendimiento físico. La información que se solicite se conservará en su dispositivo, así como en los servidores remotos.

      La aplicación puede utilizar servicios de terceros que recopilan información personal para identificarlo.

      <h3>Dato de registro</h3>
      Le informamos que siempre que utilice nuestros servicios, en caso de error en la aplicación, se recogen datos e información (a través de productos de terceros) en su teléfono,
      llamados "Datos de registro". Estos datos de registro pueden incluir información como la dirección del Protocolo de Internet ("IP") de su dispositivo, el nombre del dispositivo,
      la versión del sistema operativo, la configuración de la aplicación al utilizar el Servicio, la fecha y hora de uso del Servicio, entre otras estadísticas.

      <h3>Cookies</h3>
      Las cookies son archivos con una pequeña cantidad de datos que se utilizan comúnmente como identificadores únicos anónimos. Estos se envían a su navegador desde los sitios
      web que visita y se almacenan en la memoria interna de su dispositivo.

      Nuestro Servicio no utiliza estas "cookies" explícitamente. Sin embargo, la aplicación puede usar códigos de terceros y bibliotecas que usan "cookies" para recopilar
      información y mejorar los servicios. Tiene la opción de aceptar o rechazar estas cookies y saber cuándo se envía una cookie a su dispositivo.
      Si decide rechazar nuestras cookies, es posible que no pueda utilizar algunas partes de la aplicación.

      <h3>Proveedores de servicio</h3>
      Podemos contratar a empresas y terceros debido a las siguientes razones:

      - Para facilitar nuestro Servicio
      - Para proporcionar el Servicio en nuestro nombre
      - Para realizar servicios relacionados con la aplicación
      - Para ayudarnos a analizar cómo se utiliza la aplicación.

      Informamos a los usuarios de Pura Sangre CrossFit, que estos terceros pueden tener acceso a su Información personal. El motivo es, para realizar las tareas asignadas a ellos
      en nuestro nombre. Sin embargo, están obligados a no divulgar o utilizar la información para ningún otro propósito.

      <h3>Seguridad</h3>
      Valoramos su confianza al proporcionarnos su información personal, por lo que nos esforzamos por utilizar medios comercialmente aceptables para protegerla. Pero recuerde que
      ningún método de transmisión a través de Internet o de almacenamiento electrónico es 100% seguro y confiable, y no puedo garantizar su absoluta seguridad.

      <h3>Enlaces a otros sitios</h3>
      Este servicio puede contener enlaces a otros sitios. Si hace clic en un enlace de terceros, se le dirigirá a ese sitio. Tenga en cuenta que estos sitios externos no son
      operados por nosotros. Por lo tanto, le recomiendo encarecidamente que revise la Política de privacidad de estos sitios web. No tenemos control ni asumimos ninguna responsabilidad
      por el contenido, las políticas de privacidad o las prácticas de los sitios o servicios de terceros.

      <h3>Privacidad Infantil</h3>
      Estos Servicios no se dirigen a personas menores de 13 años. (sabiendo esto) No recopilamos información personal identificable de niños menores de 13 años. En el caso de que
      descubramos que un niño menor de 13 años nos ha proporcionado información personal, eliminamos inmediatamente esta información de nuestros servidores. Si usted es un padre o
      tutor y sabe que su hijo nos ha proporcionado información personal, comuníquese con nosotros para que podamos realizar las acciones necesarias.

      <h3>Cambios a esta política de privacidad</h3>
      Podemos actualizar nuestra Política de Privacidad de vez en cuando. Por lo tanto, se recomienda revisar esta página periódicamente para ver si hay cambios. Le notificaremos
      cualquier importante cambio mediante la publicación de la nueva Política de privacidad en esta página y también a través de las plataformas Play Store y App Store.
      Estos cambios entrarán en vigencia inmediatamente después de su publicación en esta página.

